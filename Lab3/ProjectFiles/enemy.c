void drawEnemy(tContext sContext, int16_t x, int16_t y)
{
    int i, j, height, width;
	int numberOfPixels;
    uint32_t enemyOneChannel[180];
	static int16_t last_x, last_y;

	numberOfPixels = sizeof(enemy)/sizeof(unsigned char);

	height = 6;
	width = 10;
	
	
	j = 0;
    for (i = 0; i < numberOfPixels - 3; i+=3)
    {
        enemyOneChannel[j] = (enemy[i]<<16) + (enemy[i+1]<<8) + (enemy[i+2]);
        j++;
    }
    
    GrContextBackgroundSet(&sContext, ClrBlack);
    
	
	GrContextForegroundSet(&sContext, ClrBlack);
	for (i = 0; i < height; i++)
	{
		for (j = 0; j < width; j++)
		{
			GrPixelDraw(&sContext,j+last_x,i+last_y);
		}
	}
	
    for (i = 0; i < height; i++)
    {
        for (j = 0; j < width; j++)
        {
            GrContextForegroundSet(&sContext, enemyOneChannel[i*width + j]);
            GrPixelDraw(&sContext,j+x,i+y);
        }
    }
	
	last_x = x;
	last_y = y;
}


void Enemy(tContext sContext)
{
	static int16_t dx = 2;
	static int16_t x = 20, y = 100;

	drawEnemy(sContext, x, y);
	
	x += dx;
	
	if (x > 110)
		dx *= -1;
	if (x < 20)
		dx *= -1;

}
